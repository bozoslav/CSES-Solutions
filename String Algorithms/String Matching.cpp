vector<int> z_function(string s)
{ // u vector<int> z sprema koliko slova sa pocetka je nasao od od te pozicije nadalje, ako se trazi pattern u velikom stringu onda se stavi pattern + 'â—‡' + string
    int n = (int)s.length();
    vector<int> z(n);
    for (int i = 1, l = 0, r = 0; i < n; ++i)
    {
        if (i <= r)
            z[i] = min(r - i + 1, z[i - l]);
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }
    return z;
}

signed main()
{
    cin.tie(NULL)->sync_with_stdio(false);
    cin.exceptions(cin.failbit);

    string a, b;
    cin >> a >> b;

    int damir = b.length();
    b = b + "*" + a;

    ll ans = 0;
    vector<int> res = z_function(b);
    for (int x : res)
        if (x == damir)
            ++ans;

    cout << ans;

    return 0;
}